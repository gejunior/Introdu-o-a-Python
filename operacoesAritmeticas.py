n1 = 10
n2 = 5
# Operações Aritméticas

print("Adição: %",  n1 + n2)  # Adição = 15
print("Subtração: ",  n1 - n2)  # Subtração = 5
print("Multiplicação: ",  n1 * n2)  # Multiplicação  = 50
print("Divisão: ",  n1 / n2)  # Divisão = 2.0
print("Divisão inteira: ",  n1 // n2)  # Divisão inteira = 2
print("Módulo: ",  n1 % n2)  # Módulo = 0
print("Exponenciação: ", n1 ** n2) # Exponenciação = 100000

print(n1 + n2 * 2)  # Operadores de precedência = 20
print((n1 + n2) * 2)  # Operadores de precedência = 30

x = (n1 + n2) * 4
print(x)
